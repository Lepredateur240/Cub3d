void init_map(int argc, char** argv, t_game *game)
{
	int i;
	int y;

	i = 0;
	game->data.map = malloc(sizeof(int *) * 26);
	while (i < 24)
	{
		game->data.map[i] = malloc(sizeof(int) * 24);
		i++;
	}
	i = 0;
	while (i < 24)
	{
		y = 0;
		while (y < 24)
		{
			game->data.map[i][y] = worldMap[i][y];
			y++;
		}
		i++;
	}
	game->data.map[25] = NULL; // Mur rouge
}
